"""
Script pour rechercher les r√©sultats BAC en lot √† partir d'un fichier de matricules
Usage: python recherche_batch.py [fichier_matricules]
"""

import sys
import time
import os
import requests
from bs4 import BeautifulSoup
import re


def lire_matricules(fichier_path):
    """
    Lit les matricules depuis un fichier texte
    Format attendu: nom:matricule ou juste matricule
    """
    matricules = []
    
    try:
        with open(fichier_path, 'r', encoding='utf-8') as f:
            for ligne_num, ligne in enumerate(f, 1):
                ligne = ligne.strip()
                if not ligne or ligne.startswith('#'):
                    continue
                
                # G√©rer les formats: "nom:matricule" ou juste "matricule"
                if ':' in ligne:
                    nom, matricule = ligne.split(':', 1)
                    matricule = matricule.strip()
                    nom = nom.strip()
                else:
                    matricule = ligne.strip()
                    nom = f"Candidat_{matricule}"
                
                # V√©rifier que le matricule est valide
                if matricule.isdigit():
                    matricules.append((nom, matricule))
                else:
                    print(f"‚ö†Ô∏è  Ligne {ligne_num}: Matricule invalide '{matricule}' ignor√©")
    
    except FileNotFoundError:
        print(f"‚ùå Fichier '{fichier_path}' non trouv√©")
        return []
    except Exception as e:
        print(f"‚ùå Erreur lors de la lecture du fichier: {e}")
        return []
    
    return matricules


def extraire_info_bac(numero_candidat):
    """
    Recherche et extrait les informations d√©taill√©es du BAC
    """
    url = f'https://www.mauribac.com/fr/bac-2024-uKolupoGL/numero/{numero_candidat}/'
    
    headers = {
        'User-Agent': 'Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/91.0.4472.124 Safari/537.36',
        'Accept': 'text/html,application/xhtml+xml,application/xml;q=0.9,image/webp,*/*;q=0.8',
        'Accept-Language': 'fr-FR,fr;q=0.9,en;q=0.8',
        'Accept-Encoding': 'gzip, deflate, br',
        'Connection': 'keep-alive',
        'Upgrade-Insecure-Requests': '1',
    }
    
    try:
        response = requests.get(url, headers=headers, timeout=10)
        response.raise_for_status()
        response.encoding = 'utf-8'
        
        soup = BeautifulSoup(response.content, 'html.parser', from_encoding='utf-8')
        text = soup.get_text()
        
        # Extraire les informations
        info = {
            'statut': 'Non d√©termin√©',
            'admission': 'Non trouv√©',
            'serie': 'Non sp√©cifi√©e',
            'moyenne': 'Non disponible'
        }
        
        # Rechercher le statut et l'admission
        text_lower = text.lower()
        if re.search(r'ŸÜÿßÿ¨ÿ≠|r√©ussi|admis|decision.*admis', text, re.IGNORECASE):
            info['statut'] = 'Admis'
            info['admission'] = 'Admis'
        elif re.search(r'ÿ±ÿßÿ≥ÿ®|√©chec|refus√©|decision.*refus√©', text, re.IGNORECASE):
            info['statut'] = '√âchec'
            info['admission'] = 'Refus√©'
        elif numero_candidat in text and ('bac' in text_lower or 'decision' in text_lower):
            # Si on trouve le num√©ro avec BAC mais pas de statut clair
            info['statut'] = 'Trouv√© (statut √† v√©rifier)'
            info['admission'] = '√Ä v√©rifier'
        
        # Rechercher la s√©rie
        if 'ÿßŸÑÿπŸÑŸàŸÖ ÿßŸÑÿ∑ÿ®ŸäÿπŸäÿ©' in text or 'sciences naturelles' in text.lower() or 'sn' in text.lower():
            info['serie'] = 'BAC - Sciences naturelles (SN)'
        elif 'ÿßŸÑÿ±Ÿäÿßÿ∂Ÿäÿßÿ™' in text or 'sciences math√©matiques' in text.lower() or 'sm' in text.lower():
            info['serie'] = 'BAC - Sciences math√©matiques (SM)'
        elif 'ÿßŸÑÿ¢ÿØÿßÿ®' in text or 'lettres' in text.lower():
            info['serie'] = 'BAC - Lettres'
        
        # Rechercher la moyenne
        moyenne_match = re.search(r'moyenne?\s*[:\-]?\s*(\d+[.,]\d+)', text, re.IGNORECASE)
        if not moyenne_match:
            moyenne_match = re.search(r'ÿßŸÑŸÖÿπÿØŸÑ\s*(\d+\.?\d*)', text)
        if moyenne_match:
            info['moyenne'] = moyenne_match.group(1)
        
        return True, info
        
    except requests.exceptions.RequestException:
        return False, {'statut': 'Erreur de connexion', 'admission': 'Erreur', 'serie': 'Erreur', 'moyenne': 'Erreur'}
    except Exception:
        return False, {'statut': 'Erreur inconnue', 'admission': 'Erreur', 'serie': 'Erreur', 'moyenne': 'Erreur'}


def rechercher_resultats_batch(matricules):
    """
    Recherche les r√©sultats pour une liste de matricules
    """
    print(f"üéì Recherche en lot - {len(matricules)} candidat(s) √† traiter")
    print("=" * 60)
    
    resultats = []
    
    for i, (nom, matricule) in enumerate(matricules, 1):
        print(f"\n[{i}/{len(matricules)}] Traitement de {nom} (#{matricule})")
        print("-" * 50)
        
        try:
            # Utiliser la nouvelle fonction d'extraction
            success, info = extraire_info_bac(matricule)
            
            if success:
                resultats.append({
                    'nom': nom,
                    'matricule': matricule,
                    'statut': info['statut'],
                    'admission': info['admission'],
                    'serie': info['serie'],
                    'moyenne': info['moyenne']
                })
                print(f"‚úÖ R√©sultat trouv√© pour {nom}")
                print(f"   üìä Statut: {info['statut']}")
                print(f"   üéì Admission: {info['admission']}")
                print(f"   üìö S√©rie: {info['serie']}")
                print(f"   üìà Moyenne: {info['moyenne']}")
            else:
                resultats.append({
                    'nom': nom,
                    'matricule': matricule,
                    'statut': info['statut'],
                    'admission': 'Non trouv√©',
                    'serie': 'Non sp√©cifi√©e',
                    'moyenne': 'Non disponible'
                })
                print(f"‚ùå Aucun r√©sultat pour {nom}: {info['statut']}")
        
        except Exception as e:
            print(f"‚ùå Erreur pour {nom}: {e}")
            resultats.append({
                'nom': nom,
                'matricule': matricule,
                'statut': f'Erreur: {e}',
                'admission': 'Erreur',
                'serie': 'Erreur',
                'moyenne': 'Erreur'
            })
        
        # Pause entre les requ√™tes pour respecter le serveur
        if i < len(matricules):
            print("‚è≥ Pause de 3 secondes...")
            time.sleep(3)
    
    return resultats


def afficher_resume(resultats):
    """
    Affiche un r√©sum√© des r√©sultats
    """
    print("\n" + "=" * 60)
    print("üìä R√âSUM√â DE LA RECHERCHE EN LOT")
    print("=" * 60)
    
    total = len(resultats)
    admis = len([r for r in resultats if r['admission'] == 'Admis'])
    refuses = len([r for r in resultats if r['admission'] == 'Refus√©'])
    non_trouves = len([r for r in resultats if r['admission'] == 'Non trouv√©'])
    erreurs = len([r for r in resultats if r['admission'] == 'Erreur'])
    
    print(f"üìà Total candidats trait√©s: {total}")
    print(f"‚úÖ Admis: {admis}")
    print(f"‚ùå Refus√©s: {refuses}")
    print(f"‚ùì Non trouv√©s: {non_trouves}")
    print(f"‚ö†Ô∏è  Erreurs: {erreurs}")
    
    # Statistiques par s√©rie
    series = {}
    for r in resultats:
        if r['serie'] != 'Erreur' and r['serie'] != 'Non sp√©cifi√©e':
            series[r['serie']] = series.get(r['serie'], 0) + 1
    
    if series:
        print(f"\nüìö R√©partition par s√©rie:")
        for serie, count in series.items():
            print(f"  ‚Ä¢ {serie}: {count} candidat(s)")
    
    if erreurs > 0:
        print("\nüîç D√©tail des erreurs:")
        for r in resultats:
            if r['admission'] == 'Erreur':
                print(f"  ‚Ä¢ {r['nom']} (#{r['matricule']}): {r['statut']}")


def sauvegarder_resultats(resultats, fichier_sortie="resultats_batch.txt"):
    """
    Sauvegarde les r√©sultats dans un fichier
    """
    try:
        with open(fichier_sortie, 'w', encoding='utf-8') as f:
            f.write("R√âSULTATS RECHERCHE BAC - TRAITEMENT EN LOT\n")
            f.write("=" * 50 + "\n")
            f.write(f"Date: {time.strftime('%Y-%m-%d %H:%M:%S')}\n\n")
            
            for r in resultats:
                f.write(f"Nom: {r['nom']}\n")
                f.write(f"Matricule: {r['matricule']}\n")
                f.write(f"Statut: {r['statut']}\n")
                f.write(f"Admission: {r['admission']}\n")
                f.write(f"S√©rie: {r['serie']}\n")
                f.write(f"Moyenne: {r['moyenne']}\n")
                f.write("-" * 30 + "\n")
        
        print(f"üìÅ R√©sultats sauvegard√©s dans: {fichier_sortie}")
    
    except Exception as e:
        print(f"‚ùå Erreur lors de la sauvegarde: {e}")


def main():
    """
    Fonction principale
    """
    # D√©terminer le fichier √† utiliser
    if len(sys.argv) > 1:
        fichier_matricules = sys.argv[1]
    else:
        fichier_matricules = "etu.txt"
    
    if not os.path.exists(fichier_matricules):
        print(f"‚ùå Fichier '{fichier_matricules}' non trouv√©")
        print("Usage: python recherche_batch.py [fichier_matricules]")
        return
    
    print(f"üìÇ Lecture du fichier: {fichier_matricules}")
    
    # Lire les matricules
    matricules = lire_matricules(fichier_matricules)
    
    if not matricules:
        print("‚ùå Aucun matricule valide trouv√©")
        return
    
    print(f"üìã {len(matricules)} matricule(s) trouv√©(s):")
    for nom, matricule in matricules:
        print(f"  ‚Ä¢ {nom}: {matricule}")
    
    # Demander confirmation
    reponse = input(f"\nü§î Continuer avec la recherche pour {len(matricules)} candidat(s)? (o/N): ")
    if reponse.lower() not in ['o', 'oui', 'y', 'yes']:
        print("üö´ Recherche annul√©e")
        return
    
    # Effectuer les recherches
    resultats = rechercher_resultats_batch(matricules)
    
    # Afficher le r√©sum√©
    afficher_resume(resultats)
    
    # Sauvegarder les r√©sultats
    sauvegarder_resultats(resultats)
    
    print("\nüéØ Traitement termin√©!")


if __name__ == "__main__":
    main()
